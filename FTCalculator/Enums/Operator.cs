namespace FTCalculator.Enums
{
    /// <summary>
    /// The Operator enum represents simple calculator operations
    /// </summary>
    public enum Operator
    {
        /// <summary>
        /// Represents the lack of an operator
        /// </summary>
        NoOperator,

        /// <summary>
        /// Represents the addition (+) operator
        /// </summary>
        Add,

        /// <summary>
        /// Represents the subtraction (-) operator
        /// </summary>
        Subtract,

        /// <summary>
        /// Represents the division (/) operator
        /// </summary>
        Divide,

        /// <summary>
        /// Represents multiplication (*) operator
        /// </summary>
        Multiply,

        /// <summary>
        /// Represents the factorial (!) operator
        /// </summary>
        Factorial
    }
}
